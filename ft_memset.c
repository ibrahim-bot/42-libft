#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	char			*str;

	str = b;
	i = 0;
	while (i < len)
	{
		*str = (unsigned char)c;
		i++;
		str++;
	}
	return (b);
}
