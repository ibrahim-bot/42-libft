#include "libft.h"

void	putchar_fds(char c, int fd)
{
	write(fd, &c, 1);
}

void	ft_putstr_fd(char *s, int fd)
{
	int i;

	i = 0;
	while (s[i])
	{
		putchar_fds(s[i], fd);
		i++;
	}
}
